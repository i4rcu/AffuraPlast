function resolveKey(dict, key) {
  return key.split(".").reduce(function (o, k) { return o ? o[k] : undefined; }, dict);
}

function setLang(lang) {
  if (!I18N[lang]) lang = "tr";
  var dict = getDict(lang);

  document.querySelectorAll("[data-i18n]").forEach(function (el) {
    var v = resolveKey(dict, el.getAttribute("data-i18n"));
    if (typeof v === "string") el.textContent = v;
  });

  document.querySelectorAll("[data-i18n-ph]").forEach(function (el) {
    var v = resolveKey(dict, el.getAttribute("data-i18n-ph"));
    if (typeof v === "string") el.setAttribute("placeholder", v);
  });

  document.documentElement.lang = lang;
  document.documentElement.dir = lang === "ar" ? "rtl" : "ltr";
  document.title = dict.meta.title;

  document.querySelectorAll(".lang-btn").forEach(function (btn) {
    btn.classList.toggle("active", btn.getAttribute("data-lang") === lang);
  });

  try { localStorage.setItem("affura-lang", lang); } catch (e) { /* gizli mod */ }

  /* Metin uzunlukları değişince scroll tetikleyicileri yenile */
  if (window.ScrollTrigger) window.ScrollTrigger.refresh();
}

document.addEventListener("DOMContentLoaded", function () {
  var saved = "ar";
  try { saved = localStorage.getItem("affura-lang") || "ar"; } catch (e) { /* yoksay */ }
  /* ?lang=en gibi bir parametre kayıtlı tercihi geçersiz kılar (paylaşılabilir link) */
  var urlLang = new URLSearchParams(window.location.search).get("lang");
  if (urlLang && I18N[urlLang]) saved = urlLang;
  setLang(saved);

  document.querySelectorAll(".lang-btn").forEach(function (btn) {
    btn.addEventListener("click", function () {
      setLang(btn.getAttribute("data-lang"));
    });
  });
});
